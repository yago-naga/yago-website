<h1>YAGO: A High-Quality Knowledge Base</h1>

<p>
    YAGO is a huge semantic knowledge base, derived from Wikipedia WordNet and GeoNames. Currently, YAGO has knowledge
    of more than 10 million entities (like persons, organizations, cities, etc.) and contains more than 120 million
    facts about these entities.
</p>

