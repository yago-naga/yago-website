<h2 id="access">How can I access Yago?</h2>
<p>There are several ways to access YAGO:</p>
<ol>
    <li>Online in person on our Web Interface.</li>
    <li>Online through the SPARQL interface.</li>
    <li>
        Offline by downloading the TTL version of YAGO, and loading it into an RDF triple store (e.g.,
        Jena).
    </li>
    <li>
        Offline by downloading the TSV version of YAGO, loading it into a database with the script provided
        at the bottom of the page, and using SQL.
    </li>
</ol>

