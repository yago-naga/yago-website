<h2 id="what-is-yago">What is Yago?</h2>
<p>AGO is an ontology, i.e., a database with knowledge about the real world. YAGO contains both entities (such as
    movies, people, cities, countries, etc.) and facts about these entities (who played in which movie, which city is
    located in which country, etc.). All in all, YAGO contains 10 million entities and 120 million facts.</p>


<h2 id="yago-special">What is so special about Yago?</h2>
<p>YAGO is special in several ways:</p>
<ol>
    <li>
        The accuracy of YAGO has been manually evaluated, proving a confirmed accuracy of 95%. Every
        relation is annotated with its confidence value.
    </li>
    <li>
        YAGO is an ontology that is anchored in time and space. YAGO attaches a temporal dimension and a
        spacial dimension to many of its facts and entities.
    </li>
    <li>
        In addition to a taxonomy, YAGO has thematic domains such as "music" or "science".
    </li>
</ol>


